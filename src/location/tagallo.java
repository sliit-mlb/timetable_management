    //fetch 
    private void fetch() {
        tagged.clear();

        try {
            DefaultTableModel dtm = (DefaultTableModel) tagroom.getModel();
            dtm.setRowCount(0);

            if (con == null) {
                con = DBConnection.getDBConnection();
            }

            String sql = "select * from room";
            st = con.createStatement();
            ResultSet rs = st.executeQuery(sql);

            while (rs.next()) {
                Vector vv = new Vector();

                String gette = rs.getString("roomID");
                String gette2 = rs.getString("tag");

                vv.add(gette);
                vv.add(gette2);

                dtm.addRow(vv);

            }

        } catch (Exception e) {
            e.printStackTrace();
        }

    }

