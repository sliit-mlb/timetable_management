    private void fetch() {
        tagged.clear();

        try {
            DefaultTableModel dtm = (DefaultTableModel) contable.getModel();
            dtm.setRowCount(0);

            if (con == null) {
                con = DBConnection.getDBConnection();
            }

            String sql = "select * from consecutivesessions";
            st = con.createStatement();
            ResultSet rs = st.executeQuery(sql);

            while (rs.next()) {
                Vector vv = new Vector();

                String gette = rs.getString("session1");
                String gette2 = rs.getString("session2");
                String gette3 = rs.getString("roomID");

                vv.add(gette);
                vv.add(gette2);
                vv.add(gette3);

                dtm.addRow(vv);

            }

        } catch (Exception e) {
            e.printStackTrace();
        }

    }

