    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:

        String sess1 = ses1.getText().trim();
        String sess2 = ses2.getText().trim();
        String room = rooms.getSelectedItem().toString().trim();

        if (!room.isEmpty() && (!sess1.isEmpty() || sess1.isEmpty() || sess2.isEmpty() || !sess1.isEmpty())) {
            try {
                if (con == null) {
                    con = DBConnection.getDBConnection();
                }
                String sql = "select * from consecutivesessions where session1='" + sess1 + "'";
                st = con.createStatement();
                ResultSet rs = st.executeQuery(sql);
                if (rs.first()) {
                    update(room, sess1);
                    DefaultTableModel model = (DefaultTableModel) contable.getModel();
                    model.setRowCount(0);
                    fetch();
                    alert("Update was successful");

                } else {
                    alert("There is no such session ID ", "Update error");
                    clear();
                }

            } catch (ClassNotFoundException | SQLException ex) {
                Logger.getLogger(consession.class.getName()).log(Level.SEVERE, null, ex);
            }
        } else {
            alert("There is nothing to update :(", "No row selected");
        }
    }//GEN-LAST:event_jButton3ActionPerformed

