    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        if(JOptionPane.showConfirmDialog(null, "Confirm to delete","Warning",JOptionPane.YES_NO_OPTION) == JOptionPane.YES_NO_OPTION){
        try {
            int i = jTable1.getSelectedRow();
            String prgramname = (String) jTable1.getValueAt(i, 0);

            ResultSet rs = DBConnection.search("select * from studentsubgroup where subGroupNo='" + prgramname + "'");
            if (rs.next()) {
                JOptionPane.showMessageDialog(this, "This Sub Group No is referenced in Other Table....!", "Done", JOptionPane.ERROR_MESSAGE);
            } else {
                DBConnection.iud("delete from subgroupno where subGroupNo='" + prgramname + "'");
                JOptionPane.showMessageDialog(this, "Sub Group No Deleted....!", "Done", JOptionPane.INFORMATION_MESSAGE);
                loaddata4();
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Please Select Row", "Done", JOptionPane.ERROR_MESSAGE);
        }
        }
    }//GEN-LAST:event_jButton4ActionPerformed

