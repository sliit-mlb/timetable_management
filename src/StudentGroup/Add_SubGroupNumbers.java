    private void loaddata4() {

        try {
            DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
            dtm.setRowCount(0);

            ResultSet rs = DBConnection.search("select * from subgroupno");

            while (rs.next()) {
                Vector vv = new Vector();

//                vv.add(rs.getString("yearAndSemester"));
                String gette = rs.getString("subGroupNo");

                vv.add(gette);
                dtm.addRow(vv);

            }

        } catch (Exception e) {
            e.printStackTrace();
        }
    }
