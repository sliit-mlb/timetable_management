    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            if (jTextField1.getText().equals("")) {
                JOptionPane.showMessageDialog(this, "Please Enter Sub Group No....!", "Done", JOptionPane.ERROR_MESSAGE);

            } else {
                ResultSet rs = DBConnection.search("select * from subgroupno where subGroupNo='" + jTextField1.getText() + "'");
                if (rs.next()) {
                    JOptionPane.showMessageDialog(this, "This Sub Group No is Already Entered....!", "Done", JOptionPane.ERROR_MESSAGE);
                } else {
                    DBConnection.iud("insert into subgroupno(subGroupNo) values('" + jTextField1.getText() + "')");

                    JOptionPane.showMessageDialog(this, "Sub Group No Added....!", "Done", JOptionPane.INFORMATION_MESSAGE);
                    loaddata4();
                }
                jTextField1.setText("");
                jTextField1.grabFocus();
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

