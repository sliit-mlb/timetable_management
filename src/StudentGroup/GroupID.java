    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {

            String yearandsem = jComboBox1.getSelectedItem().toString();
            String programmez = jComboBox2.getSelectedItem().toString();
            String grpnoz = jComboBox3.getSelectedItem().toString();

            String connuadno = yearandsem + "." + programmez + "." + grpnoz;
            ResultSet rs5 = DBConnection.search("select * from studentgroup where groupID='" + connuadno + "'");
            if (rs5.next()) {
                JOptionPane.showMessageDialog(this, "This Group ID is Already Entered....!", "Done", JOptionPane.ERROR_MESSAGE);
            } else {
                String programname = "";
                String yas = "";
                String grpno = "";
                ResultSet rs = DBConnection.search("select * from groupno where groupNo='" + grpnoz + "'");

                if (rs.next()) {
                    grpno = rs.getString("groupNo");
                }
                ResultSet rs2 = DBConnection.search("select * from programme where programmeName='" + programmez + "'");
                if (rs2.next()) {
                    programname = rs2.getString("programmeName");
                }
                ResultSet rs3 = DBConnection.search("select * from yearandsem where yearAndSemester='" + yearandsem + "'");
                if (rs3.next()) {
                    yas = rs3.getString("yearAndSemester");
                }

                DBConnection.iud("INSERT INTO `timetable_management`.`studentgroup`\n"
                        + "            (`groupID`,\n"
                        + "             `yearAndSem`,\n"
                        + "             `programme`,\n"
                        + "             `groupNo`)\n"
                        + "VALUES ('" + connuadno + "',\n"
                        + "        ' " + yas + "',\n"
                        + "        ' " + programname + " ',\n"
                        + "        '" + grpno + " ');");
                JOptionPane.showMessageDialog(this, "New Group ID Added....!", "Done", JOptionPane.INFORMATION_MESSAGE);
                this.dispose();
                Thread.sleep(100);
                new GroupID().setVisible(true);
                loadgrp();

            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        try {
            int i = jTable1.getSelectedRow();
            String prgramname = (String) jTable1.getValueAt(i, 0);
            System.out.println(prgramname);
            ResultSet rs = DBConnection.search("select * from studentsubgroup where allid='"+prgramname+"'");
            if (rs.next()) {
                JOptionPane.showMessageDialog(this, "This  Group ID is referenced in Other Table....!", "Done", JOptionPane.ERROR_MESSAGE);
            } else {
                DBConnection.iud("delete from studentgroup where groupID='" + prgramname + "'");
                JOptionPane.showMessageDialog(this, "Group ID Deleted....!", "Done", JOptionPane.INFORMATION_MESSAGE);
                loadgrp();
            }

        } catch (Exception e) {
            e.printStackTrace();
        }

    }//GEN-LAST:event_jButton2ActionPerformed

