    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {

            String year = "";
            String semester = "";
            if (jCheckBox3.isSelected()) {
                year = "Y1";
            } else if (jCheckBox4.isSelected()) {
                year = "Y2";
            } else if (jCheckBox2.isSelected()) {
                year = "Y3";
            } else if (jCheckBox1.isSelected()) {
                year = "Y4";
            }

            if (jRadioButton2.isSelected()) {
                semester = "S1";
            } else if (jRadioButton1.isSelected()) {
                semester = "S2";

            }
            if (year == "") {
                JOptionPane.showMessageDialog(this, "Please Select Year....!", "Done", JOptionPane.ERROR_MESSAGE);

            } else if (semester == "") {
                JOptionPane.showMessageDialog(this, "Please Select Semester....!", "Done", JOptionPane.ERROR_MESSAGE);

            } else {
                String conqd = year + "." + semester;
                ResultSet rs = DBConnection.search("select * from yearandsem where yearAndSemester='" + conqd + "'");
                if (rs.next()) {
                    JOptionPane.showMessageDialog(this, "This Year and Semester is Already Entered....!", "Done", JOptionPane.ERROR_MESSAGE);
                } else {
                    DBConnection.iud("insert into yearandsem(yearAndSemester) values('" + conqd + "')");

                    JOptionPane.showMessageDialog(this, "Yead and Semester Added....!", "Done", JOptionPane.INFORMATION_MESSAGE);
                    loaddata();
                }
                this.dispose();
                Thread.sleep(100);
                new Add_AcademicYear().setVisible(true);
            }

        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if(JOptionPane.showConfirmDialog(null, "Confirm to delete","Warning",JOptionPane.YES_NO_OPTION) == JOptionPane.YES_NO_OPTION){
        try {
            int i = jTable2.getSelectedRow();
            String yrs = (String) jTable2.getValueAt(i, 2);
            String conz = " " + yrs;
           // System.out.println(yrs);
            ResultSet rs = DBConnection.search("select * from studentgroup where yearAndSem='" + conz + "'");
            if (rs.next()) {
                JOptionPane.showMessageDialog(this, "This Year and Semester is referenced in Other Table....!", "Done", JOptionPane.ERROR_MESSAGE);
            } else {
                DBConnection.iud("delete from yearandsem where yearAndSemester='" + yrs + "'");
                JOptionPane.showMessageDialog(this, "Yead and Semester Deleted....!", "Done", JOptionPane.INFORMATION_MESSAGE);
                loaddata();

            }
        } catch (Exception e) {
           JOptionPane.showMessageDialog(this, "Please Select Row", "Done", JOptionPane.ERROR_MESSAGE); 
        }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

