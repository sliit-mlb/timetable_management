    private void loaddata() {
        try {
            DefaultTableModel dtm = (DefaultTableModel) jTable2.getModel();
            dtm.setRowCount(0);

            ResultSet rs = DBConnection.search("select * from yearandsem");

            while (rs.next()) {
                Vector vv = new Vector();

//                vv.add(rs.getString("yearAndSemester"));
                String gette = rs.getString("yearAndSemester");

                String ar[] = gette.split("");
                vv.add(ar[0] + ar[1]);
                vv.add(ar[3] + ar[4]);
                vv.add(gette);
                dtm.addRow(vv);

            }

        } catch (Exception e) {
            e.printStackTrace();
        }

    }
