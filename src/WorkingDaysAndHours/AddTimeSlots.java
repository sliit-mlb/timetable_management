    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:

        PreparedStatement stmt1, stmt2 = null;
        ResultSet rs = null;
        try {
            String type = bg.getSelection().getActionCommand();
            String startTimeHour = comboHour.getSelectedItem().toString();
            String startTimeMin = comboMinutes.getSelectedItem().toString();
            String batch = comboSelectBatch.getSelectedItem().toString();

            if(batch == "Select"){
                JOptionPane.showMessageDialog(this,"Please select a batch","Error",JOptionPane.ERROR_MESSAGE);
            }else if (startTimeHour == "--" || startTimeMin == "--") {
                JOptionPane.showMessageDialog(this,"Please select start time properly","Error",JOptionPane.ERROR_MESSAGE);
            }else {
                String startTime = startTimeHour+":"+startTimeMin+":00";
                String ty="";
                if(type=="30"){
                    ty="T";
                }else if(type=="60"){
                    ty="O";
                }

                String idRet = "SELECT workingID FROM workingdaysandhours WHERE batch=?";
                stmt1 = con.prepareStatement(idRet);
                stmt1.setString(1, batch);
                rs = stmt1.executeQuery();

                ArrayList<String> ids = new ArrayList<String>();
                while (rs.next()) {
                    ids.add(rs.getString(1));
                }
                
                String insertQuery = "INSERT INTO timeslots VALUES(?,?,?)";
                stmt2 = con.prepareStatement(insertQuery);
                stmt2.setString(1, startTime);
                stmt2.setString(2, ty);
                stmt2.setString(3, ids.get(0));
                stmt2.execute();
                
                JOptionPane.showMessageDialog(this, "Successfully Inserted.");
                
                this.setVisible(false);
                new AddTimeSlots().setVisible(true);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this,"Duplicate Time Slots","Error",JOptionPane.ERROR_MESSAGE);
            
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnAddActionPerformed

