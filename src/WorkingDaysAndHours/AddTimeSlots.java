    public void setEndTime(){
        String stH = comboHour.getSelectedItem().toString();
        String stM = comboMinutes.getSelectedItem().toString();
        String type = bg.getSelection().getActionCommand();
        
        if(!(stH == "--" || stM == "--")){
            int stHr = Integer.parseInt(stH);
            int stMs = Integer.parseInt(stM);
            
            if(type=="30"){
                stMs+=30;
                if(stMs>59){
                    stHr++;
                    stMs-=60;
                }
            }else if(type=="60"){
                stMs+=60;
                if(stMs>59){
                    stHr++;
                    stMs-=60;
                }
            }
            
            String minutes;
            if(stMs<10){
                minutes = "0"+stMs;
            }else{
                minutes = Integer.toString(stMs);
            }
            
            txtEndTime.setText(stHr + " : " + minutes);
        }
    }
    
