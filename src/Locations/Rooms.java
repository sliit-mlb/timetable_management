private void loaddata() {
        try {
            DefaultTableModel dtm = (DefaultTableModel) jTable2.getModel();
            dtm.setRowCount(0);

            ResultSet rs = DBConnection.search("select * from rooms");

            while (rs.next()) {
                Vector vv = new Vector();

                String gette1 = rs.getString("Rid");
                String gette2 = rs.getString("Rname");
                String gette3 = rs.getString("Buildingname");
                String gette4 = rs.getString("Floors");
                String gette5 = rs.getString("Rtype");
                String gette6 = rs.getString("Hallsize");
                String gette7 = rs.getString("Labsize");
                
                


                 vv.add(gette1);
                 vv.add(gette2);
                 vv.add(gette3);
                 vv.add(gette4);
                 vv.add(gette5);
                 vv.add(gette6);
                 vv.add(gette7);
                

                dtm.addRow(vv);

            }

        } catch (Exception e) {
            e.printStackTrace();
        }

    }


