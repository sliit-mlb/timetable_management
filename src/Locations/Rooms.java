    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        //update code
        try {
         
            int i = jTable2.getSelectedRow();
            String id = (String) jTable2.getValueAt(i, 0);
            
            String roomname = (String) jTable2.getValueAt(i, 1);
            String building = (String) jTable2.getValueAt(i, 2);
            String floors = (String) jTable2.getValueAt(i, 3);
            String roomtype = (String) jTable2.getValueAt(i, 4);
            String hallsize = (String) jTable2.getValueAt(i, 5);
            String labsize = (String) jTable2.getValueAt(i, 6);
            
                  
                 
            DBConnection.iud("update rooms SET Rname='" + roomname + "',Buildingname='" + building + "',Floors='" + floors + "',Floors='" + floors + "',Rtype='" + roomtype + "',Hallsize='" + hallsize + "',Labsize='" + labsize + "' where Rid='" + id+ "'");
                
            
           
            JOptionPane.showMessageDialog(this, " Updated the record!", "Done", JOptionPane.INFORMATION_MESSAGE);
            this.dispose();
            new Rooms().setVisible(true);

        } catch (Exception e) {
           JOptionPane.showMessageDialog(this, " Error....!", "Done", JOptionPane.INFORMATION_MESSAGE);
        }
        
    }//GEN-LAST:event_jButton4ActionPerformed

